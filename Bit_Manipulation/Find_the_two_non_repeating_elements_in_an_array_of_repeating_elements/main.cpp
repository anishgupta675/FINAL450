class Solution
{
public:
    vector<int> singleNumber(vector<int> nums) 
    {
        // Code here.
        int x = 0;
        vector<int> res(2, 0);
        for(int i : nums) x^= i;
        x&= -x;
        for(int i : nums) {
            if(x & i) res[0]^= i;
            else res[1]^= i;
        }
        sort(res.begin(), res.end());
        return res;
    }
};

